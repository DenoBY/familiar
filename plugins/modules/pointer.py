"""Форма указателя мыши (OSC 22).

Пока включён mouse-tracking, kitty подменяет курсор стрелкой —
сигнал «мышь у приложения». В просмотрщике текст выделяется своим
drag-select, поэтому возвращаем текстовый курсор (I-beam). Через
стек push/pop, чтобы на выходе восстановить прежнюю форму, а не
навязывать default.
"""


def push_text_pointer() -> str:
    return '\x1b]22;>text\x1b\\'


def pop_pointer() -> str:
    return '\x1b]22;<\x1b\\'
