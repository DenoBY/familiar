"""Копирование в системный буфер обмена: общий модуль всех китов.

Терминал сам кладёт текст в буфер по escape-последовательности
OSC 52 — работает и через ssh, и внутри tmux, в отличие от вызова
pbcopy в подпроцессе.
"""

import base64


def osc52(text: str) -> str:
    """OSC 52: терминал положит text в системный буфер обмена."""
    b64 = base64.b64encode(text.encode('utf-8')).decode('ascii')
    return '\x1b]52;c;' + b64 + '\x07'
